from qiskit import QuantumCircuit

def fm_pauli_xz(x: np.ndarray, layers: int = 1) -> QuantumCircuit:
    x = np.asarray(x, dtype=float)
    n = len(x)
    qc = QuantumCircuit(n, name="U_pauli_xz")
    qc.h(range(n))
    for _ in range(layers):
        for i in range(n):
            qc.rx(2 * np.pi * x[i], i)
            qc.rz(2 * np.pi * x[i] ** 2, i)
        if n >= 2:
            for i in range(n):
                for j in range(i + 1, n):
                    angle = 2 * np.pi * x[i] * x[j]
                    # Implements an X_i Z_j interaction using basis change:
                    # H_i RZZ(i,j) H_i
                    qc.h(i)
                    qc.rzz(angle, i, j)
                    qc.h(i)
    return qc

def fm_iqp(x: np.ndarray, layers: int = 1) -> QuantumCircuit:
    x = np.asarray(x, dtype=float)
    n = len(x)
    qc = QuantumCircuit(n, name="U_iqp")
    for _ in range(layers):
        qc.h(range(n))
        for i in range(n):
            qc.rz(2 * np.pi * x[i], i)
        if n >= 2:
            for i in range(n):
                for j in range(i + 1, n):
                    qc.rzz(2 * np.pi * x[i] * x[j], i, j)
    return qc

def fm_hamiltonian(x: np.ndarray, layers: int = 1) -> QuantumCircuit:
    # This does not explicitly construct H(x).
    # It implements a first-order product formula / Trotter-style evolution
    # under an implicit Hamiltonian:
    # H(x) = sum_i x_i Z_i + sum_i x_i^2 X_i + sum_i x_i*x_{i+1} Z_i Z_{i+1}
    x = np.asarray(x, dtype=float)
    n = len(x)

    qc = QuantumCircuit(n, name="U_hamiltonian")

    for layer in range(layers):
        scale = layer + 1

        # Data-dependent local fields:
        # Z field uses x_i
        # X field uses x_i^2
        for i in range(n):
            qc.rz(2 * np.pi * scale * x[i], i)
            qc.rx(2 * np.pi * scale * x[i] ** 2, i)

        # Data-dependent nearest-neighbor Ising coupling:
        # ZZ term uses x_i * x_{i+1}
        if n >= 2:
            for i in range(n - 1):
                qc.rzz(2 * np.pi * scale * x[i] * x[i + 1], i, i + 1)

    return qc

def fm_random_kitchen_sinks(
    x: np.ndarray,
    layers: int = 1,
    seed: int = 123
) -> QuantumCircuit:
    x = np.asarray(x, dtype=float)
    n = len(x)

    rng = np.random.default_rng(seed)

    # Fixed random parameters for this n and layers
    w_rx = rng.normal(size=(layers, n))
    b_rx = rng.uniform(0, 2 * np.pi, size=(layers, n))

    w_ry = rng.normal(size=(layers, n))
    b_ry = rng.uniform(0, 2 * np.pi, size=(layers, n))

    w_rz = rng.normal(size=(layers, n))
    b_rz = rng.uniform(0, 2 * np.pi, size=(layers, n))

    qc = QuantumCircuit(n, name="U_random_kitchen_sinks")

    for layer in range(layers):
        for i in range(n):
            qc.rx(2 * np.pi * w_rx[layer, i] * x[i] + b_rx[layer, i], i)
            qc.ry(2 * np.pi * w_ry[layer, i] * x[i] + b_ry[layer, i], i)
            qc.rz(2 * np.pi * w_rz[layer, i] * x[i] + b_rz[layer, i], i)

        if n >= 2:
            for i in range(n - 1):
                qc.cx(i, i + 1)

    return qc

def build_feature_map_override(x: np.ndarray, kind: str, layers: int) -> QuantumCircuit:
    k = (kind or "ry").strip().lower()
    if k == "pauli_xz" : return fm_pauli_xz(x, layers)
    if k == "iqp" : return fm_iqp(x, layers)
    if k == "hamiltonian" : return fm_hamiltonian(x, layers)
    if k == "random" : return fm_random_kitchen_sinks(x, layers)
    raise ValueError(f"Unknown feature map: {kind}")

dqc1.build_feature_map = build_feature_map_override

