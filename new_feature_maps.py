def parse_dqc1_metrics(stdout):
    metrics = {
        "DQC1-pre ARI": None,
        "DQC1-pre NMI": None,
        "DQC1-full ARI": None,
        "DQC1-full NMI": None,
    }

    for line in stdout.splitlines():
        lower = line.lower()

        ari = re.search(r"ARI\s*=\s*(-?\d+\.\d+|-?\d+)", line, re.IGNORECASE)
        nmi = re.search(r"NMI\s*=\s*(-?\d+\.\d+|-?\d+)", line, re.IGNORECASE)

        # Matches: DQC1 (ΔH-only)-moons
        if "dqc1" in lower and "h-only" in lower:
            if ari:
                metrics["DQC1-pre ARI"] = float(ari.group(1))
            if nmi:
                metrics["DQC1-pre NMI"] = float(nmi.group(1))

        # Matches: DQC1-FULL-moons
        elif "dqc1-full" in lower or "dqc1 full" in lower:
            if ari:
                metrics["DQC1-full ARI"] = float(ari.group(1))
            if nmi:
                metrics["DQC1-full NMI"] = float(nmi.group(1))

    return metrics

def run_feature_map_dataset_sweep(feature_map, datasets, n=300, verbose=False):
    rows = []

    old_feature_map = dqc1.FEATURE_MAP
    dqc1.FEATURE_MAP = feature_map.strip().lower()

    try:
        for dataset_name in datasets:
            print(f"[RUNNING] feature_map={feature_map}, dataset={dataset_name}")

            all_sets = dqc1.toy_sets(
                n=n,
                noise=dqc1.DATA_NOISE,
                random_state=dqc1.RANDOM_STATE
            )

            selected = None

            for X, y, name in all_sets:
                if name == dataset_name:
                    selected = (X, y, name)
                    break

            if selected is None:
                available = [name for _, _, name in all_sets]
                raise ValueError(f"Dataset '{dataset_name}' not found. Available: {available}")

            X, y, name = selected
            Xn = dqc1.zero_mean_to_unit_interval(X)

            model = dqc1.DQC1EntropyCluster(
                init_clusters=dqc1.INIT_CLUSTERS,
                n_init_per_seed=dqc1.N_INIT,
                kernel_shots=dqc1.DQC1_KERNEL_SHOTS,
                exact_sv_kernel=bool(dqc1.DQC1_EXACT_KERNEL),
                purity_shots=dqc1.DQC1_PURITY_SHOTS,
                exact_sv_purity=bool(dqc1.DQC1_EXACT_PURITY),
                kernel_workers=dqc1.DQC1_KERNEL_WORKERS,
                purity_workers=dqc1.DQC1_PURITY_WORKERS,
                gpu_ids=None,
                random_state=dqc1.RANDOM_STATE,
                compress_rank=dqc1.DQC1_COMP_RANK,
            )

            buffer = io.StringIO()

            with redirect_stdout(buffer):
                model.fit(X)

                pre_labels = dqc1.labels_from_clusters(X.shape[0], model.clusters_)
                pre_scores = dqc1.eval_scores(Xn, y, pre_labels)

                print(
                    f"    DQC1 (ΔH-only)-{dataset_name} | "
                    f"K={len(model.clusters_):2d}  "
                    f"ARI={pre_scores['ARI']:.3f}  "
                    f"NMI={pre_scores['NMI']:.3f}"
                )

                model.fit_full(X)

                full_labels = model.labels_full_
                full_scores = dqc1.eval_scores(Xn, y, full_labels)

                print(
                    f"  DQC1-FULL-{dataset_name} (K*={model.Kstar_}) | "
                    f"K={model.Kstar_:2d}  "
                    f"ARI={full_scores['ARI']:.3f}  "
                    f"NMI={full_scores['NMI']:.3f}"
                )

            stdout = buffer.getvalue()

            if verbose:
                print(stdout)

            metrics = parse_dqc1_metrics(stdout)

            rows.append({
                "Feature Map": feature_map,
                "Dataset": dataset_name,
                **metrics,
            })

    finally:
        dqc1.FEATURE_MAP = old_feature_map

    return pd.DataFrame(rows)

def plot_feature_map_dataset_sweep(df, feature_map=None, save_path=None):
    plot_df = df.copy()

    if feature_map is None:
        feature_map = plot_df["Feature Map"].iloc[0]

    ax = plot_df.plot(
        x="Dataset",
        y=[
            "DQC1-pre ARI",
            "DQC1-pre NMI",
            "DQC1-full ARI",
            "DQC1-full NMI",
        ],
        kind="bar",
        figsize=(10, 5)
    )

    plt.xlabel("Dataset")
    plt.ylabel("Score")
    plt.title(f"DQC1 Results Across Datasets Using {feature_map} Feature Map")
    plt.ylim(-0.05, 1)
    plt.grid(True, axis="y")
    plt.xticks(rotation=30, ha="right")
    plt.legend()
    plt.tight_layout()

    if save_path is not None:
        plt.savefig(save_path, dpi=300, bbox_inches="tight")

    plt.show()

